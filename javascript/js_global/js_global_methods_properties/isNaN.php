    <div class="mark">
        In JavaScript NaN is short for "Not-a-Number". <br><br>

        In JavaScript, NaN is a number that is not a legal number. <br><br>

        The isNaN() method returns true if a value is NaN. <br><br>

        The isNaN() method converts the value to a number before testing it.
