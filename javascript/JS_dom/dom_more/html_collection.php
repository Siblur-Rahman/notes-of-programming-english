<h2>DOM HTMLCollection<a href="https://www.w3schools.com/jsref/dom_obj_htmlcollection.asp" target="_blank">Details</a></h2>
<div class="mark">
    An HTMLCollection is an array-like collection (list) of HTML elements. <br>

    The elements in a collection can be accessed by index (starts at 0). <br>

    The length Property returns the number of elements in the collection. <br>
