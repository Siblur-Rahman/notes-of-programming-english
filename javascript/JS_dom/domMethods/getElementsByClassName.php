<p class="accordion">getElementsByClassName</p>
   <div class="panel">
   <h2>getElementsByClassName <span class="itc"><a href="https://www.w3schools.com/jsref/met_document_getelementsbyclassname.asp"  target="_blank">Details</a></span></h2>
   <div class="mark">
      The getElementsByClassName() method returns a collection of elements with a specified class name(s). <br>

      The getElementsByClassName() method returns an <span class="itc">HTMLCollection</span>. <br>

      The getElementsByClassName() property is read-only. <br>
   </div>
   <h2>Syntax</h2>
   <div class="code_p">
      document.getElementsByClassName("classname")
   </div>
</div>
